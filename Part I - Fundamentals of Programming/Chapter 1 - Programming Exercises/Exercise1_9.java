/*
 * Chapter 1 - Exercise 1.9
 * (Area and perimeter of a rectangle) Write a program that displays the area and
 * perimeter of a rectangle with the width of 4.5 and height of 7.9 using the follow-
 * ing formula:
 *
 *                           area = width * height
 * 
 */

public class Exercise1_9 {
	public static void main(String[] args) {
		System.out.println("The area of a rectangle with width of 4.5 and height of 7.9 is " + (4.5 * 7.9));
		System.out.println("The perimeter of a rectangle with width of 4.5 and height of 7.9 is " + ((4.5 + 7.9) * 2 ));
	}
}